package com.rankforge.server.dto;

/**
 * Response DTO for log processing endpoint
 * Author bageshwar.pn
 * Date 2024
 */
public class ProcessLogResponse {
    
    private String jobId;
    private String status;
    private String message;

    public ProcessLogResponse() {
    }

    public ProcessLogResponse(String jobId, String status, String message) {
        this.jobId = jobId;
        this.status = status;
        this.message = message;
    }

    public String getJobId() {
        return jobId;
    }

    public void setJobId(String jobId) {
        this.jobId = jobId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
